        <h2 class="title less-space-bottom width-small pretty-purple">Fundraising Team</h2>

        <br />

        <p class="background-green-light">The Montessori way of teaching is a unique method that requires very specific and expensive equipment. The Pre-school is a charitable, not-for-profit making organisation and raises funds through events arranged by the Fundraising Team. This enables our pre-school to purchase additional Montessori equipment, which helps to keep the school fees at an affordable rate.</p>

        <p>The Fundraising Team meets every 6-8 weeks, dependent on the events that need to be discussed and arranged. There is always a member of staff at the meetings, so you also get to meet your child's teachers informally. <strong>New parents are always needed as we are constantly brainstorming for new fundraising ideas!</strong></p>

