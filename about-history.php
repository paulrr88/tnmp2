        <h2 class="title less-space-bottom width-small pretty-blue">Maria Montessori</h2>

        <br />

        <p class="background-orange-light"><strong>Maria Montessori lived from 1870 to 1952, the first woman doctor of medicine in Italy</strong>. Her observations of the way children learn naturally by investigating their environment led her to develop a new system of education in which various aspects of development are stimulated by special pieces of equipment. </p>

        <br />

        <div class="centre-400">

          <?php perch_content('Image 1'); ?>

        </div>

        <p>Giving up her medical practice to pursue educational reform, <strong>Montessori opened her first school in 1906</strong> called the 'Casa dei Bambini' or 'The Children's House'. Her success in teaching children from deprived and neglected backgrounds there earned her international fame, and later in her life she travelled extensively giving lectures about her methods.</p>

        <p>Montessori schools and colleges can be found today all around the world, embracing children of every age and background.</p>

