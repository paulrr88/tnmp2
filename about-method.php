        <h2 class="title less-space-bottom width-small pretty-green">Montessori Method Summary</h2>

        <br />

        <p class="background-purple"><strong>The Montessori Method aims essentially to help children develop themselves by directing their natural curiosity and thirst for knowledge</strong>.</p>

        <p>We use unique equipment originally developed by Maria Montessori to let them explore <strong>colour, texture, shape, length, area, volume, letters, words and numbers</strong>. And of course we encourage more traditional expressive activities like <strong>painting, drawing and craftwork</strong>.</p>

        <div class="centre-600">

          <?php perch_content('Image 1'); ?>

        </div>

        <p><strong>In the main, the children themselves choose what activities to do and when</strong>, so that during what Montessori calls their "sensitive periods" they will naturally maximise their learning. One of the key roles of the Montessori teacher is to ensure that children are helped through these particularly receptive times.</p>

        <p class="background-orange">The Montessori Method is not just to do with the physical equipment though. <strong>It encompasses everything from our tone of voice in the classroom, to how we interact with with the children, and giving them responsibility for getting out and putting away their work</strong>. Developing independence is a vital part of education and we go to great lengths to ensure that everything in the classroom is on a "child scale". Because of this, the children see the classroom as "their" space, not ours.</p>

        <br />

        <div class="centre-600">

          <?php perch_content('Image 2'); ?>

        </div>

        <p>If this all sounds a bit nebulous, the results can best be appreciated by a personal visit to the classroom - seeing is believing! We also have some booklets describing the Montessori Method in more detail which you are welcome to take. (Sorry, we can't publish it here for copyright reasons!)</p>

        <p class="backtotop"><a href="#top">Back to top</a></p>



        <a name="develop"></a>

        <h2 class="title less-space-bottom width-small pretty-purple">Development Areas</h2>

        <p class="centre">The Method focuses on five developmental areas:</p>

        <div class="centre-400">

          <?php perch_content('Image 3'); ?>

        </div>

        <p class="centre"><strong>Practical life skills</strong> which includes task organisation, personal care, care for the environment, grace, courtesy and physical movement.</p>

        <div class="centre-400">

          <?php perch_content('Image 4'); ?>

        </div>

        <p class="centre"><strong>Sensorial</strong> which covers ordering, classification and description of sensory impressions in relation to length, width, temperature, mass, colour, pitch, etc.</p>

        <div class="centre-400">

          <?php perch_content('Image 5'); ?>

        </div>

        <p class="centre"><strong>Mathematics</strong> makes use of manipulative materials to enable the child to internalise concepts of number, symbols, sequence, operations and memorisation of basic facts.</p>

        <div class="centre-400">

          <?php perch_content('Image 6'); ?>

        </div>

        <p class="centre"><strong>Language arts</strong> includes oral language development, written expression, reading, the study of grammar, creative dramatics, and children's literature.</p>

        <div class="centre-400">

          <?php perch_content('Image 7'); ?>

        </div>

        <p class="centre"><strong>Cultural activities</strong> expose the child to basics in geography, history and life sciences. Music, art and movement education are part of the integrated cultural curriculum.</p>

        <p>Of course the children don't know any of this, they just enjoy the activities!</p>

        <br />

        <p class="background-orange">If you want to know more, please take a look at our additional page on <a href="learning/">our equipment and learning activities</a>.</p>



