        <a name="prep"></a>
        
        <h2 class="title">How can I prepare my child for starting at The New Montessori Pre-school?</h2>
        
        <ul class="sitemap">
            <li>Visit the Pre-school with your child. Observe the other children and how they order their day. Talk about how, when at Pre-school you can have drinks and snacks and make new friends.</li>
            <li>Discuss the pre-school with your child. Talk about the staff you have met and the other children who were there.</li>
            <li>Arm your child with practical information like how to hang up their coat. Dress your child comfortably in suitable clothes. Getting dirty is a big concern for young children. </li>
            <li>Practice leaving your child with a trusted friend/grandparent. Make sure the child is aware of what is happening. Short separations are a good way for your child to develop the understanding and trust that is needed to ensure they have a good time when you are not there. </li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="settle"></a>
        
        <h2 class="title">How will my child settle into The New Montessori Pre-school?</h2>
        
        <ul class="sitemap">
            <li>Your child will have four-one-hour visits to accustom themselves with the pre-school.</li>
            <li>At these sessions your child will have the chance to explore the classroom. The staff are there to deal with any problems your child might have.</li>
            <li>Afterwards discuss the child's day; encourage chat about equipment/friends/activities they liked. Encourage and support by emphasising how our pre-school is their school. If the child is apprehensive discuss what you will do together after pre-school, maybe a treat/visit to the park.</li>
            <li>If your child mentions a child's name, ask our School manager to introduce you to their parents. One recognisable face can mean a lot to a new child; invite the child to meet up with them outside of pre-school.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="minimum"></a>
        
        <h2 class="title">What is the minimum session per week?</h2>
        
        <ul class="sitemap">
            <li>Our pattern of attendance asks for all children starting at our pre-school to be in attendance for at least two sessions. These sessions can be taken as two morning or afternoon sessions or one full day. If your child is in receipt of 2/3/4-year-old funding it is expected that they attend for at least 13 hours a week.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="food"></a>
        
        <h2 class="title">What will my child eat and how do you deal with fussy eating if that is an issue?</h2>
        
        <ul class="sitemap">
            <li>Your child will be offered various fruits and a biscuit. Your child will be offered a glass of milk or water. If your child is a "fussy" eater or has special dietary needs, parents may bring in the child's snack and a drink (no chocolate or nuts please). We endeavour to work with the parents ensuring every child is provided with a snack throughout their session.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="behaviour"></a>
        
        <h2 class="title">How do you deal with bad behaviour and how do you promote good behaviour?</h2>
        
        <ul class="sitemap">
            <li>As Montessorians we role model the best behaviour and provide lots of one-to-one time to encourage awareness in the child. Maria Montessori's put in place many exercises for the child to develop their self-awareness and understanding of boundaries and behaviour. </li>
            <li>If your child repeatedly displays "disruptive" inappropriate behaviour our School Manager and the child's key worker will put in place a strategy to work from within our pre-school. Our School Manager will discuss all of this with the parents and child prior to implementation.</li>
            <li>For general misbehaviour we have a behaviour box which children are shown one-to-one and a discussion takes place.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="quiet"></a>
        
        <h2 class="title">Will my child be able to have a bit of 'quiet time' if it all gets too much during   the day? Where will he/she be able to do this?</h2>
        
        <ul class="sitemap">
            <li>Our pre-school has many quiet spaces provided to encourage our children to rest/nap and reflect throughout their session.</li>
            <li>If a child needs a lay down, we provide a bed and invite them to take a nap in our closed classroom-supported by an adult throughout their time asleep.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="outdoor"></a>
        
        <h2 class="title">How much outdoor play do the children have during the day?</h2>
        
        <ul class="sitemap">
            <li>Our garden is open throughout all sessions and children learn to take turns throughout their session. </li>
            <li>Each child can have as many turns as they choose to, and all children are encouraged to go out at least once in their session.</li>
            <li>Our pre-school has various outdoor weather clothes for your children to borrow, if needed.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="reports"></a>
        
        <h2 class="title">Will I get daily written or verbal progress reports?</h2>
        
        <ul class="sitemap">
            <li>Our pre-school uses an online system called the My Montessori child. We will provide you with a login and password during the first weeks of term. It is updated with photos and comments on activities your child has taken part in at least twice a week.</li>
            <li>If a more in-depth report is required, the parent can book an appointment with our School Manager or a progress meeting. You may also ask us questions/make comments and make home observations using this online system.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
