        <a name="behaviourprocess"></a>
        
        <h2 class="title">If I am working on a particular behaviour or process at home, will you continue to implement this within the setting?</h2>
        
        <ul class="sitemap">
            <li>Yes, through the My Montessori child/e-mail/face-to face we can work in partnership with parents regarding processes at home.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="ratios"></a>
        
        <h2 class="title">Are the staff ratios in line with the government guidelines?</h2>
        
        <ul class="sitemap">
            <li>As a Montessori pre-school we follow a higher ratio than expected by Ofsted a 1-6 ratio for children aged 3 &amp; 4 years of age. 1-3 for children aged 2 years of age.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="illness"></a>
        
        <h2 class="title">What happens if staff are off ill, how are the ratios maintained?</h2>
        
        <ul class="sitemap">
            <li>Our Management Committee have ensured that our managers are out of ratio so in the event of a class member being ill a manager can cover the classroom.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="groups"></a>
        
        <h2 class="title">Will my child be kept with the same group of children for the duration of the session?</h2>
        
        <ul class="sitemap">
            <li>Montessori pre-schools use vertical grouping which means it is the children who choose who and what age children they mix with in their session.</li>
            <li>Through our project work we encourage our older children to work together to prepare them for a primary school setting.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="turnover"></a>
        
        <h2 class="title">What is the staff turnover like at the pre-school? What has it been in the past and how long has my child's soon to be key worker been employed at the pre-school?</h2>
        
        <ul class="sitemap">
            <li>Our pre-school has an extremely low turnover off staff. Most of our staff have been with us for over seven years.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="toys"></a>
        
        <h2 class="title">My child is attached to an old favourite toy. I am sure he will want to take it to pre-school. Should I try to wean she/he off it now?</h2>
        
        <ul class="sitemap">
            <li>Growing up happens gradually, not on the day your child starts pre-school. Allow her/him to bring the toy. The staff will put it on her/his peg for safekeeping. If she/he needs to return to it, they can.  A cuddle from a favourite toy is very reassuring to the child. One day she/he will ask to put it in her/his bag or leave it at home.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="refusal"></a>
        
        <h2 class="title">My daughter/son was so excited about her new pre-school and sailed in on her first few sessions. The following week she/he refused point blank to go.</h2>
        
        <ul class="sitemap">
            <li>Many children come to pre-school interested in the new environment and all the new activities. Once the child has accustomed themselves to the pre-school, they can sometimes start to wonder 'what does mummy do when I'm here' and become aware of what they are missing and then maybe not want to attend pre-school. Encourage your child prior to coming; explain what you are going to do while they are at pre-school and what you and the child can do after pre-school.</li>
            <li>Other children immediately don't want to be separated from their careers and will not want to come at all. This is an adjustment time and slowly with the work of parents and teachers, the child will develop a bond with the pre-school and feel safe and secure – and importantly – feel that they belong.</li>
        </ul>
        
        <p><a href="#top">Back to top</a></p>
        
        <hr />
        
        <a name="fees"></a>
        
        <h2 class="title">How are your fees structured?</h2>
        
        <ul class="sitemap">
            <li>Our pre-school offers the settling in visits free of charge to ensure your child is settled and ready for pre-school before we provide you with an invoice.</li>
            <li>If your child is not in receipt of funding it is our hourly fee times the hours your child is in attendance.</li>
            <li>Our pre-school splits the funding over the 43 weeks we are open (9 weeks closed for Winter, Spring and Summer holidays). West Sussex County Council funding only covers 38 weeks.</li>
            <li>If your child is in receipt of WSCC funding, the funding will be shown as zero on your invoice, and the amount received from WSCC will have been withdrawn from your invoice prior to you receiving it.</li>
        </ul>
        
