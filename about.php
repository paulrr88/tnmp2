        <p>The New Montessori Pre-school provides an holistic approach to children's education so as to develop the whole child, <strong>physically, socially, intellectually, spiritually and emotionally</strong>. We celebrate each child's individuality and have a deep understanding of their development needs.</p>

        <p>Our aim is to <strong>help the child develop life skills</strong> including confidence, independence and liberty. We encourage and foster respect for others through positive role models, lessons in grace and courtesy and by child initiated ground rules. With you, we can ensure the best start by building solid foundations for your child to possess a life-long love of learning.</p>

